public class lampupintar {
    
    public static void main(String[] args) {
        boolean adaOrang;

        // apabila adaOrang = true
        adaOrang = true;
        if (adaOrang) {
            System.out.println("lampu menyala");
        } else {
            System.out.println("lampu mati");
        }

        // apabila adaOrang = false
        adaOrang = false;
        if (adaOrang) {
            System.out.println("lampu menyala");
        } else {
            System.out.println("lampu mati");
        }
    }
}

